# Enumeration for CC1101 states
class CC1101State(Enum):
    IDLE = 0b000
    RX = 0b001
    TX = 0b010
    FSTXON = 0b011
    CALIBRATE = 0b100
    SETTLING = 0b101
    RXFIFO_OVERFLOW = 0b110
    TXFIFO_UNDERFLOW = 0b111

@dataclass
class CC1101Status:
    FIFO_BYTES_AVAILABLE: int = False
    STATE: CC1101State = CC1101State.IDLE
    CHIP_RDYn: bool = True

@dataclass
class CC1101TxBytes:
    NUM_TXBYTES: int
    TXFIFO_UNDERFLOW: bool

@dataclass
class CC1101RxBytes:
    NUM_RXBYTES: int
    RXFIFO_OVERFLOW: bool
